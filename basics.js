// basics.js

// Declaring variables using var
var name = "Faiza";
var age = 20;

// Declaring variables using let
let isStudent = true;
let subjects = ["Math", "Science", "English"];

// Declaring a constant using const
const college = {
  name: "ABC Institute",
  location: "Mumbai",
  course: "Computer Engineering"
};

// Logging all variables to the console
console.log("Name (var):", name);               // String
console.log("Age (var):", age);                 // Number
console.log("Is Student (let):", isStudent);    // Boolean
console.log("Subjects (let):", subjects);       // Array
console.log("College (const):", college);       // Object